static void meter_label_cb(lv_event_t * e) {
    lv_obj_draw_part_dsc_t * dsc = lv_event_get_draw_part_dsc(e);
    if (dsc->part == LV_PART_TICKS && dsc->text) {
        int32_t val = (int32_t)dsc->value;
        int32_t abs_val = val < 0 ? -val : val;
        lv_snprintf(dsc->text, dsc->text_length, "%d", (int)abs_val);
    }
}

static bool g_meter_cb_registered = false;

void ui_init() {
    eez_flow_init(assets, sizeof(assets), (lv_obj_t **)&objects, sizeof(objects), images, sizeof(images), actions);
}

void ui_tick() {
    eez_flow_tick();
    tick_screen(g_currentScreen);
    if (!g_meter_cb_registered && objects.obj0) {
        lv_obj_add_event_cb(objects.obj0, meter_label_cb, LV_EVENT_DRAW_PART_BEGIN, NULL);
        g_meter_cb_registered = true;
    }
}